    private void botonModificarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonModificarProductoActionPerformed
        int fila2 = tablaInventario.getSelectedRow();
        if (fila2 > -1){
        int fila = tablaInventario.getSelectedRow();
        Frame f = JOptionPane.getFrameForComponent(this);
        ModificarInventario dialog = new ModificarInventario(f, true);
        dialog.txtNombreProducto.setText(tablaInventario.getValueAt(fila, 1).toString());
        dialog.txtCantidad.setText(tablaInventario.getValueAt(fila, 2).toString());
        dialog.txtDescripcion.setText(tablaInventario.getValueAt(fila, 3).toString());
        dialog.comboImpuestoUpdate.setSelectedItem(tablaInventario.getValueAt(fila, 4).toString());
        dialog.txtPrecio.setText(tablaInventario.getValueAt(fila, 5).toString());
        dialog.idProducto.setText(tablaInventario.getValueAt(fila, 0).toString());
        dialog.show();
        } else {
            new rojerusan.RSNotifyAnimated("ERROR", "Seleccione uno", 5, RSNotifyAnimated.PositionNotify.TopLef, RSNotifyAnimated.AnimationNotify.UpBottom, RSNotifyAnimated.TypeNotify.ERROR).setVisible(true);

        }
    }//GEN-LAST:event_botonModificarProductoActionPerformed

    private void botonReporteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonReporteActionPerformed
        int fila4 = tablaInventario.getSelectedRow();
        if (fila4 > -1){
        try {
            JasperReport jr = (JasperReport) JRLoader.loadObject(getClass().getResource("/reportes/Inventario.jasper"));
            
            Map parametro = new HashMap();
                parametro.put("IMAGEN", getClass().getResourceAsStream("/imagenes/FacturaProducto.png"));
            JasperPrint jp = JasperFillManager.fillReport(jr, parametro, conexionActual);
            JasperViewer jv = new JasperViewer(jp, false);
            jv.setVisible(true);
            jv.setTitle("Reporte de Inventario");
        } catch (JRException ex) {
             System.err.println("Error: "+ex.getMessage());
    }
        } else {
            new rojerusan.RSNotifyAnimated("ERROR", "Seleccione uno", 5, RSNotifyAnimated.PositionNotify.TopLef, RSNotifyAnimated.AnimationNotify.UpBottom, RSNotifyAnimated.TypeNotify.ERROR).setVisible(true);

        }
    }//GEN-LAST:event_botonReporteActionPerformed

