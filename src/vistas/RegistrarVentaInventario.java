public static void Calcular(){
    int precioInven = 0, sumaInven = 0, cant = 0 , suma = 0, multiplo = 0, totalGen =0;
    int subTotal = 0, imp = 0, sub = 0;
    
    
    for (int i = 0; i < tablaVentaInven.getRowCount(); i++) {
        precioInven = Integer.parseInt(tablaVentaInven.getValueAt(i,2).toString());
        cant = Integer.parseInt(tablaVentaInven.getValueAt(i,3).toString());
        imp = Integer.parseInt(tablaVentaInven.getValueAt(i, 4).toString());
        multiplo = precioInven * cant;
        suma += multiplo;
        
    }
    
    totalGen = suma + sumaInven;
    txtTotalGen.setText(String.valueOf(totalGen));
    
    sub = (totalGen * imp)/100; 
    subTotal = totalGen-sub;
     txtSubTotal.setText(String.valueOf(subTotal));
     txtTotalImpuesto.setText(String.valueOf(sub));
}
