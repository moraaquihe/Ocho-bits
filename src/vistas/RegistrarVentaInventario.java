    private void botonRegistrarOrdenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonRegistrarOrdenActionPerformed
        int tabla = tablaVentaInven.getSelectedRowCount();
        if (tabla > -1){
        if (txtTotalGen.getText().length()==0 || lblCliente.getText().length()==0) {
            new rojerusan.RSNotifyFade("ADVERTENCIA", "Registre todos los campos", 5, RSNotifyFade.PositionNotify.TopLef, RSNotifyFade.TypeNotify.WARNING).setVisible(true);
        } else {
        funciones.funcionesVentas.guardarVentasInven();
        
        try {
            JasperReport jr = (JasperReport) JRLoader.loadObject(getClass().getResource("/reportes/FacturaProducto.jasper"));
            System.out.println("Id Alumno: ");
            Map parametro = new HashMap();
                parametro.put("IDFactura", idVentasInven.getText()); 
                parametro.put("IMAGEN", getClass().getResourceAsStream("/imagenes/FacturaProducto.png"));
            JasperPrint jp = JasperFillManager.fillReport(jr, parametro, conexionActual);
            JasperViewer jv = new JasperViewer(jp, false);
            jv.setVisible(true);
            jv.setTitle("Factura Producto");
        } catch (JRException ex) {
             System.err.println("Error: "+ex.getMessage());
    }
        this.dispose();
        } 
            

        }
    }//GEN-LAST:event_botonRegistrarOrdenActionPerformed

