    private void botonModificarPaisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonModificarPaisActionPerformed
            String tabla = "PAIS";
            String evento = "UPDATE";
            hora = Principal.lblhora.getHora();
            fecha = Principal.lblfecha.getFecha(); 
        if(this.txtModificarPais.getText().length() == 0){
        new rojerusan.RSNotifyFade("ERROR", "Ingrese el pais", 5, RSNotifyFade.PositionNotify.TopLef, RSNotifyFade.TypeNotify.ERROR).setVisible(true);
    } else {
        sentenciasPaises s = new sentenciasPaises();
        s.setId_pais(this.txtid.getText());
        s.setN_pais(this.txtModificarPais.getText());

        if (funcionesPaises.isUpdate(s)){
            new rojerusan.RSNotifyFade("EXITO", "Se ha registrado", 5, RSNotifyFade.PositionNotify.TopLef, RSNotifyFade.TypeNotify.SUCCESS).setVisible(true);
            funcionesPaises.setListar("");
            funciones.funcionesAuditoria.auditoria(fecha, hora, evento, tabla, login.funcionesLogin.idFuncionario);
            this.dispose();
        }
    }
    }//GEN-LAST:event_botonModificarPaisActionPerformed

