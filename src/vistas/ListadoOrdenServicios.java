            RegistrarVentaServicio p = new RegistrarVentaServicio();
            vistas.Principal.jDesktopPane1.add(p);
            funciones.funcionesVentas.setListarDetVentasServicios(tablaListadoSer.getValueAt(fila, 0).toString());
