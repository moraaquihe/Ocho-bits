    private void botonVerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonVerActionPerformed
        int fila = tablaMensaje.getSelectedRow();
        
        VerMensajeCompleto b = new VerMensajeCompleto(new JFrame(), true);
        
        b.txtNombreApellido.setText(tablaMensaje.getValueAt(fila, 1).toString());
        b.txtEmail.setText(tablaMensaje.getValueAt(fila, 2).toString());
        b.txtCelular.setText(tablaMensaje.getValueAt(fila, 3).toString());
        b.txtAsunto.setText(tablaMensaje.getValueAt(fila, 4).toString());
        b.txtMensaje.setText(tablaMensaje.getValueAt(fila, 5).toString());
        b.lblEstado.setText(tablaMensaje.getValueAt(fila, 6).toString());
        b.idMsj.setText(tablaMensaje.getValueAt(fila, 0).toString());
        
        b.show();
    }//GEN-LAST:event_botonVerActionPerformed

    private void botonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEliminarActionPerformed
        int fila = tablaMensaje.getSelectedRow();
        sentenciasMensajesWeb s = new sentenciasMensajesWeb();
        s.setId_mensaje(tablaMensaje.getValueAt(fila, 0).toString());
        if (JOptionPane.showConfirmDialog(this, "Desea eliminar registro?", "",JOptionPane.OK_CANCEL_OPTION)==JOptionPane.OK_OPTION){
            if (funciones.funcionesMensajesWeb.isDelete(s)){
                funciones.funcionesMensajesWeb.setListarMensajes();
            }
        }
    }//GEN-LAST:event_botonEliminarActionPerformed


