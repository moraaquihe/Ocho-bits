    private void botonEliminarDepartamentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEliminarDepartamentoActionPerformed
            String tabla = "DEPARTAMENTO";
            String evento = "DELETE";
            hora = Principal.lblhora.getHora();
            fecha = Principal.lblfecha.getFecha();
        int selec2 = tablaDepartamento.getSelectedRow();
        if (selec2 >-1) {
        int fila = tablaDepartamento.getSelectedRow();
        sentenciasDepartamentos s = new sentenciasDepartamentos();
        s.setId_departamento(tablaDepartamento.getValueAt(fila, 0).toString());
        if (JOptionPane.showConfirmDialog(this, "Desea eliminar registro?", "",JOptionPane.OK_CANCEL_OPTION)==JOptionPane.OK_OPTION){
            if (funcionesDepartamentos.isDelete(s)){
                new rojerusan.RSNotifyFade("EXITO", "Se ha eliminado el departamento", selec2, RSNotifyFade.PositionNotify.TopLef, RSNotifyFade.TypeNotify.SUCCESS).setVisible(true);
                 funciones.funcionesAuditoria.auditoria(fecha, hora, evento, tabla, login.funcionesLogin.idFuncionario);
            
                funcionesDepartamentos.setListar("");
                funciones.funcionesDepartamentos.extraerIDMaxDEPAR();
                }
            }
        
        } else {
            new rojerusan.RSNotifyFade("ADVERTENCIA", "Seleccione un departamento", 5, RSNotifyFade.PositionNotify.TopLef, RSNotifyFade.TypeNotify.WARNING).setVisible(true);
        }
    }//GEN-LAST:event_botonEliminarDepartamentoActionPerformed

