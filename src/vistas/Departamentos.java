    private void botonAgregarDepartamentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAgregarDepartamentoActionPerformed
        txtAgregarDepartamento.requestFocus();
        if (txtAgregarDepartamento.getText().length()==0 || comboPais.getSelectedIndex() <=0) {
            new rojerusan.RSNotifyFade("ADVERTENCIA", "Ingrese los datos solicitados", 5, RSNotifyFade.PositionNotify.TopLef, RSNotifyFade.TypeNotify.WARNING).setVisible(true);
        } else {
        try{

            String sql= "INSERT INTO departamentos(id_departamento,n_departamento,fk_pais)"
            +"VALUES('"+idDepartamento.getText()+"', '"+txtAgregarDepartamento.getText()+"', '"+comboPais.getSelectedIndex()+"')";

            ps = conexionActual.prepareStatement(sql);

            int resultado = ps.executeUpdate();
            if (resultado > 0){
              funcionesDepartamentos.setListar("");
                txtAgregarDepartamento.setText("");
                comboPais.setSelectedItem("Seleccione un pais");
                this.idDepartamento.setText(funcionesDepartamentos.extraerIDMaxDEPAR());
                new rojerusan.RSNotifyFade("EXITO", "Se ha registrado correctamente", 5, RSNotifyFade.PositionNotify.TopLef, RSNotifyFade.TypeNotify.SUCCESS).setVisible(true);
                
            }else{
                new rojerusan.RSNotifyFade("ERROR", "Ha ocurrido un problema al momento del guardado", 5, RSNotifyFade.PositionNotify.TopLef, RSNotifyFade.TypeNotify.ERROR).setVisible(true);
            }
        } catch (Exception e) {
            System.out.print("error"+e.getMessage());
        }
        }
    }//GEN-LAST:event_botonAgregarDepartamentoActionPerformed

    private void botonModificarDepartamentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonModificarDepartamentoActionPerformed
        int selec = tablaDepartamento.getSelectedRow();
        if (selec >-1) {
        int fila = tablaDepartamento.getSelectedRow();
        Frame f = JOptionPane.getFrameForComponent(this);
        ModificarDepartamento dialog = new ModificarDepartamento (f, true);
        dialog.txtModificarDepartamento.setText(tablaDepartamento.getValueAt(fila, 1).toString());
        dialog.comboPaisUpdate.setSelectedItem(tablaDepartamento.getValueAt(fila, 2).toString());
        dialog.txtid.setText(tablaDepartamento.getValueAt(fila, 0).toString());
        dialog.show(); 
        } else {
            new rojerusan.RSNotifyFade("ADVERTENCIA", "Seleccione un departamento", 5, RSNotifyFade.PositionNotify.TopLef, RSNotifyFade.TypeNotify.WARNING).setVisible(true);
        }
    }//GEN-LAST:event_botonModificarDepartamentoActionPerformed
