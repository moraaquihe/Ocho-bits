    private void botonRegistrarEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonRegistrarEActionPerformed
        if (txtNuevoEgreso.getText().length()==0 || txtNuevoEgresoConcepto.getText().length()==0) {
            new rojerusan.RSNotifyFade("ADVERTENCIA", "Ingrese los datos necesarios", 5, RSNotifyFade.PositionNotify.TopLef, RSNotifyFade.TypeNotify.WARNING).setVisible(true);
        } else {
            
            funciones.funcionesIngresoEgreso.guardarEgreso();
            new rojerusan.RSNotifyFade("EXITO", "Se ha registrado el egreso", 5, RSNotifyFade.PositionNotify.TopLef, RSNotifyFade.TypeNotify.SUCCESS).setVisible(true);
            funciones.funcionesIngresoEgreso.setListarEgresos(fecha);
            this.dispose();
        }
        
    }//GEN-LAST:event_botonRegistrarEActionPerformed

    private void txtNuevoEgresoConceptoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNuevoEgresoConceptoKeyTyped
        if (txtNuevoEgresoConcepto.getText().length() >=50) {
            evt.consume();
        } if(librerias.Validaciones.Letras(evt.getKeyChar()) && librerias.Validaciones.Espacio(evt.getKeyChar())) {
            evt.consume();
        }
    }//GEN-LAST:event_txtNuevoEgresoConceptoKeyTyped

