public void CalcularTotales(){
    int totalCompras = 0, totalVentas = 0, totalGen = 0;
    int sumaCompras = 0, sumasVentas = 0, totalIng = 0, totalEgr = 0, sumasIng = 0, sumasEgr = 0;
    for (int i = 0; i < tablaComprasCaja.getRowCount(); i++) {
        totalCompras = Integer.parseInt(tablaComprasCaja.getValueAt(i,1).toString());
        sumaCompras += totalCompras;
    }
    for (int i = 0; i < tablaVentasCaja.getRowCount(); i++) {
       totalVentas = Integer.parseInt(tablaVentasCaja.getValueAt(i,2).toString());
       sumasVentas += totalVentas; 
    }
     for (int i = 0; i < tablaIngresosCaja.getRowCount(); i++) {
       totalIng = Integer.parseInt(tablaIngresosCaja.getValueAt(i,1).toString());
       sumasIng += totalIng; 
    }
     for (int i = 0; i < tablaEgresosCaja.getRowCount(); i++) {
       totalEgr = Integer.parseInt(tablaEgresosCaja.getValueAt(i,1).toString());
       sumasEgr += totalEgr; 
    }
    
    totalGen = (sumasVentas + sumasIng) - (sumasEgr + sumaCompras);
    
    
    
    
     txtTotalGenCompras.setText(String.valueOf(sumaCompras));
     txtTotalGenVentas.setText(String.valueOf(sumasVentas));
     txtIngreso.setText(String.valueOf(sumasIng));
     txtEgreso.setText(String.valueOf(sumasEgr));
     txtTotalCierre.setText(String.valueOf(totalGen));
    
    
    String puntocompra = String.valueOf(sumaCompras);
    String puntoventa = String.valueOf(sumasVentas);
    String puntoegreso = String.valueOf(sumasEgr);
    String puntoingreso = String.valueOf(sumasIng);
    String puntocierre = String.valueOf(totalGen);
    
        if(puntocompra.length() > 3){
            String cadena = puntocompra.replace(".","");
            txtTotalGenCompras.setText(formateador.format(Integer.parseInt(cadena)));
        } 
        if(puntoventa.length() > 3){
            String cadena2 = puntoventa.replace(".","");
            txtTotalGenVentas.setText(formateador.format(Integer.parseInt(cadena2)));
        }
        if(puntoegreso.length() > 3){
            String cadena3 = puntoegreso.replace(".","");
            txtEgreso.setText(formateador.format(Integer.parseInt(cadena3)));
        }
        if(puntoingreso.length() > 3){
            String cadena4 = puntoingreso.replace(".","");
            txtIngreso.setText(formateador.format(Integer.parseInt(cadena4)));
        }
        if(puntocierre.length() > 3){
            String cadena5 = puntocierre.replace(".","");
            txtTotalCierre.setText(formateador.format(Integer.parseInt(cadena5)));
        }
}




