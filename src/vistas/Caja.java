    private void botonCierreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonCierreActionPerformed
        String cierre3 = librerias.SeparadorMiles.quitarPuntos(vistas.Caja.txtTotalCierre.getText());
        int cierre2 = Integer.parseInt(cierre3);
        if (cierre2 > 0) {
            String cierre = vistas.Caja.txtTotalCierre.getText();
            String fec = vistas.Caja.dateSeleccion.getText();
            String account = vistas.Caja.lblCuenta.getText();
            String number = vistas.Caja.idC.getText();
            String idcaja = vistas.Caja.idCaja.getText();
            
            String apertura = librerias.SeparadorMiles.quitarPuntos(vistas.Caja.txtSaldo.getText());
            
            
            int total = 0;
            total = cierre2 + Integer.parseInt(apertura);
            funciones.funcionesCaja.guardarCaja();
            this.dispose();
            Movimientos f = new Movimientos(new JFrame(), true);
            
            String puntomov = String.valueOf(total);
            if(puntomov.length() > 3){
            String cadena6 = puntomov.replace(".","");
            f.txtMontoMov.setText(formateador.format(Integer.parseInt(cadena6)));
        }
           // f.txtMontoMov.setText(Integer.valueOf(total).toString());
            f.txtCuentaMov.setText(account);
            f.txtFechaMov.setText(fec);
            f.idCajaMov.setText(idcaja);
            f.idCuentaMov.setText(number);
            f.show();
        } else {
            new rojerusan.RSNotifyFade("ERROR", "No se puede realizar el cierre", 5, RSNotifyFade.PositionNotify.TopLef, RSNotifyFade.TypeNotify.ERROR).setVisible(true);
        }
        
    }//GEN-LAST:event_botonCierreActionPerformed


