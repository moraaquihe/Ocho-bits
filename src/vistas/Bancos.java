    private void botonAgregarBancoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAgregarBancoActionPerformed
        txtAgregarBanco.requestFocus();
        if (txtAgregarBanco.getText().length()==0) {
            new rojerusan.RSNotifyFade("ERROR", "Ingrese el nombre del banco", 5, RSNotifyFade.PositionNotify.TopLef, RSNotifyFade.TypeNotify.ERROR).setVisible(true);
        } else {
            try{
            String sql= "INSERT INTO bancos(id_banco,n_banco)"
            +"VALUES('"+idBanco.getText()+"','"+txtAgregarBanco.getText()+"')";
            ps = conexionActual.prepareStatement(sql);
            int resultado = ps.executeUpdate();
            if (resultado > 0){
                funciones.funcionesBancos.setListar("");
                funciones.funcionesBancos.extraerIDMaxBAN();
                this.idBanco.setText(funciones.funcionesBancos.extraerIDMaxBAN());
                new rojerusan.RSNotifyAnimated("Guardado", "Se ha registrado.", 5, RSNotifyAnimated.PositionNotify.TopLef, RSNotifyAnimated.AnimationNotify.UpBottom, RSNotifyAnimated.TypeNotify.SUCCESS).setVisible(true);
        txtAgregarBanco.setText("");
            }else{
                 new rojerusan.RSNotifyAnimated("ERROR", "Ingrese el banco.", 5, RSNotifyAnimated.PositionNotify.TopLef, RSNotifyAnimated.AnimationNotify.UpBottom, RSNotifyAnimated.TypeNotify.ERROR).setVisible(true);
            }
        } catch (Exception e) {
            System.out.print("error"+e.getMessage());
        }
        }
        
    }//GEN-LAST:event_botonAgregarBancoActionPerformed

    private void botonModificarBancoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonModificarBancoActionPerformed
        int modi = tablaBanco.getSelectedRowCount();
        if (modi >-1) {
            int fila = tablaBanco.getSelectedRow();
            Frame f = JOptionPane.getFrameForComponent(this);
            ModificarBanco dialog = new ModificarBanco (f, true);
            dialog.txtModificarBanco.setText(tablaBanco.getValueAt(fila, 1).toString());
            dialog.txtid.setText(tablaBanco.getValueAt(fila, 0).toString());
            dialog.show();
        } else {
            new rojerusan.RSNotifyFade("ADVERTENCIA", "Seleccione el banco", 5, RSNotifyFade.PositionNotify.TopLef, RSNotifyFade.TypeNotify.WARNING).setVisible(true);
        }
        
    }//GEN-LAST:event_botonModificarBancoActionPerformed
