    private void botonEliminarBancoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEliminarBancoActionPerformed
        String tabla = "BANCO";
            String evento = "DELETE";
            hora = Principal.lblhora.getHora();
            fecha = Principal.lblfecha.getFecha();
        int modic = tablaBanco.getSelectedRowCount();
        if (modic >-1) {
        int fila = tablaBanco.getSelectedRow();
        sentenciasBancos s = new sentenciasBancos();
        s.setId_banco(tablaBanco.getValueAt(fila, 0).toString());
        if (JOptionPane.showConfirmDialog(this, "Desea eliminar registro?", "",JOptionPane.OK_CANCEL_OPTION)==JOptionPane.OK_OPTION){
            if (funciones.funcionesBancos.isDelete(s)){
                funciones.funcionesAuditoria.auditoria(fecha, hora, evento, tabla, login.funcionesLogin.idFuncionario);
                funciones.funcionesBancos.setListar("");
                funciones.funcionesBancos.extraerIDMaxBAN();
                new rojerusan.RSNotifyFade("ELIMINADO", "Se ha eliminado el banco", 5, RSNotifyFade.PositionNotify.TopLef, RSNotifyFade.TypeNotify.SUCCESS).setVisible(true);
        
            }
        }
        } else {
             new rojerusan.RSNotifyFade("ADVERTENCIA", "Seleccione el banco", 5, RSNotifyFade.PositionNotify.TopLef, RSNotifyFade.TypeNotify.WARNING).setVisible(true);
        }
    }//GEN-LAST:event_botonEliminarBancoActionPerformed

