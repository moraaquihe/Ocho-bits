    private void botonModificarFuncionarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonModificarFuncionarioActionPerformed
        int fila2 = tablaFuncionarios.getSelectedRow();
        if (fila2 > -1){
        int fila = tablaFuncionarios.getSelectedRow();
        Frame f = JOptionPane.getFrameForComponent(this);
        ModificarFuncionario dialog = new ModificarFuncionario(f, true);
        dialog.txtNombreFuncionario.setText(tablaFuncionarios.getValueAt(fila, 1).toString());
        dialog.txtRUCCI.setText(tablaFuncionarios.getValueAt(fila, 2).toString());
        dialog.comboCiudadUpdate.setSelectedItem(tablaFuncionarios.getValueAt(fila, 3).toString());
        dialog.txtCorreo.setText(tablaFuncionarios.getValueAt(fila, 4).toString());
        dialog.txtTelefono.setText(tablaFuncionarios.getValueAt(fila, 5).toString());
        dialog.txtDireccion.setText(tablaFuncionarios.getValueAt(fila, 6).toString());
        dialog.txtCargo.setText(tablaFuncionarios.getValueAt(fila, 7).toString());
        dialog.txtIdFuncionario.setText(tablaFuncionarios.getValueAt(fila, 0).toString());
        dialog.show();
        } else {
            new rojerusan.RSNotifyAnimated("ERROR", "Seleccione un funcionario", 5, RSNotifyAnimated.PositionNotify.TopLef, RSNotifyAnimated.AnimationNotify.UpBottom, RSNotifyAnimated.TypeNotify.ERROR).setVisible(true);

        }
    }//GEN-LAST:event_botonModificarFuncionarioActionPerformed
String hora = "";
