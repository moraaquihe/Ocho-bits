    private void botonEliminarFuncionarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEliminarFuncionarioActionPerformed
            String tabla = "FUNCIONARIO";
            String evento = "DELETE";
            hora = Principal.lblhora.getHora();
            fecha = Principal.lblfecha.getFecha();
        int fila3 = tablaFuncionarios.getSelectedRow();
        if (fila3 > -1){
        int fila = tablaFuncionarios.getSelectedRow();
        sentenciasFuncionarios s = new sentenciasFuncionarios();
        s.setId_funcionario(tablaFuncionarios.getValueAt(fila, 0).toString());
        if (JOptionPane.showConfirmDialog(this, "Desea eliminar registro?", "",JOptionPane.OK_CANCEL_OPTION)==JOptionPane.OK_OPTION){
            if (funciones.funcionesFuncionarios.isDelete(s)){
                funciones.funcionesFuncionarios.extraerIDMaxFUN();
                new rojerusan.RSNotifyAnimated("EXITO", "Se ha eliminado", 5, RSNotifyAnimated.PositionNotify.TopLef, RSNotifyAnimated.AnimationNotify.UpBottom, RSNotifyAnimated.TypeNotify.SUCCESS).setVisible(true);
                funciones.funcionesFuncionarios.setListar("");
                funciones.funcionesAuditoria.auditoria(fecha, hora, evento, tabla, login.funcionesLogin.idFuncionario);
            }
        }
        } else {
            new rojerusan.RSNotifyAnimated("ERROR", "Seleccione uno", 5, RSNotifyAnimated.PositionNotify.TopLef, RSNotifyAnimated.AnimationNotify.UpBottom, RSNotifyAnimated.TypeNotify.ERROR).setVisible(true);

        }
    }//GEN-LAST:event_botonEliminarFuncionarioActionPerformed

