public static void Calcular(){
    int precioSer = 0, sumaSer = 0, precioProd = 0, cant = 0 , suma = 0, multiplo = 0, totalGen =0;
    int subTotal = 0, imp = 0, sub = 0;
    for (int i = 0; i < tablaVentaServicios.getRowCount(); i++) {
        precioSer = Integer.parseInt(tablaVentaServicios.getValueAt(i,2).toString());
        sumaSer += precioSer;
    }
    for (int i = 0; i < tablaVentaProductos.getRowCount(); i++) {
        precioProd = Integer.parseInt(tablaVentaProductos.getValueAt(i,2).toString());
        cant = Integer.parseInt(tablaVentaProductos.getValueAt(i,3).toString());
        imp = Integer.parseInt(tablaVentaProductos.getValueAt(i, 4).toString());
        multiplo = precioProd * cant;
        suma += multiplo;
        imp += imp;
    }
    totalGen = suma + sumaSer;
    txtTotalGen.setText(String.valueOf(totalGen));   
    sub = (totalGen * imp)/100; 
    subTotal = totalGen-sub;
     txtSubTotal.setText(String.valueOf(subTotal));
     txtTotalImpuesto.setText(String.valueOf(sub));
}



    
