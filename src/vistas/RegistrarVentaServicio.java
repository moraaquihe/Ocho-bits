    private void botonRegistrarVentaSerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonRegistrarVentaSerActionPerformed
        
        int tabla = tablaVentaServicios.getSelectedRowCount();
        if (tabla > -1){
            if (txtTotalGen.getText().length()==0 || lblCliente.getText().length()==0) {
            new rojerusan.RSNotifyFade("ADVERTENCIA", "Registre todos los campos", 5, RSNotifyFade.PositionNotify.TopLef, RSNotifyFade.TypeNotify.WARNING).setVisible(true);
        } else {
            funciones.funcionesVentas.guardarVentasServicio();
            
            new rojerusan.RSNotifyAnimated("Venta exitosa!", "Se ha completado la venta", 5, RSNotifyAnimated.PositionNotify.TopLef, RSNotifyAnimated.AnimationNotify.UpBottom, RSNotifyAnimated.TypeNotify.SUCCESS).setVisible(true);
            funciones.funcionesOrdenServicios.FinalizarOrden(idOrdenSer.getText());
        try {
            JasperReport jr = (JasperReport) JRLoader.loadObject(getClass().getResource("/reportes/FacturaServicio.jasper"));
            //System.out.println("Id Alumno: ");
            Map parametro = new HashMap();
                parametro.put("IDFacturaVen", idVentaSer.getText()); 
                parametro.put("IMAGEN", getClass().getResourceAsStream("/imagenes/FacturaProducto.png"));
            JasperPrint jp = JasperFillManager.fillReport(jr, parametro, conexionActual);
            JasperViewer jv = new JasperViewer(jp, false);
            jv.setVisible(true);
            jv.setTitle("Factura Servicio");
        } catch (JRException ex) {
             System.err.println("Error: "+ex.getMessage());
    }
        
        this.dispose();
        }
           // new rojerusan.RSNotifyAnimated("ERROR", "No se puede realizar la venta", 5, RSNotifyAnimated.PositionNotify.TopLef, RSNotifyAnimated.AnimationNotify.UpBottom, RSNotifyAnimated.TypeNotify.ERROR).setVisible(true);
        }
        
        
    }//GEN-LAST:event_botonRegistrarVentaSerActionPerformed

