    private void botonRegistrarOrdenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonRegistrarOrdenActionPerformed
        if (txtDescripcion.getText().length() == 0 || txtTotal.getText().length() == 0 || comboTecnico.getSelectedIndex() < 0 || lblCliente.getText().length() == 0 || tablaOrdenServicios.getSelectedRowCount() < -1) {
                new rojerusan.RSNotifyFade("ERROR", "Ingrese todos los campos", 5, RSNotifyFade.PositionNotify.TopLef, RSNotifyFade.TypeNotify.ERROR).setVisible(true);
            } else {
                funciones.funcionesOrdenServicios.guardarOrdenServicio();
                new rojerusan.RSNotifyFade("EXITO", "Se ha registrado la orden", 5, RSNotifyFade.PositionNotify.TopLef, RSNotifyFade.TypeNotify.SUCCESS).setVisible(true);
                    try {
                        JasperReport jr = (JasperReport) JRLoader.loadObject(getClass().getResource("/reportes/OrdenServicio.jasper"));
            
                           Map parametro = new HashMap();
                        parametro.put("IDOrden", idOrdenSer.getText()); 
                        parametro.put("IMAGEN", getClass().getResourceAsStream("/imagenes/FacturaProducto.png"));
                        JasperPrint jp = JasperFillManager.fillReport(jr, parametro, conexionActual);
                        JasperViewer jv = new JasperViewer(jp, false);
                        jv.setVisible(true);
                        jv.setTitle("Reporte Ãºnico de Servicio");
                    } catch (JRException ex) {
                        System.err.println("Error: "+ex.getMessage());
                    }
            this.dispose();
            }
        
    }//GEN-LAST:event_botonRegistrarOrdenActionPerformed

